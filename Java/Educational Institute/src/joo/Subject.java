package joo;

public class Subject {
    private String Name;
    private int Grade;

    public void setName(String n) {
        this.Name = n;
    }
    public String getName() {
        return this.Name;
    }

    public void setGrade(int n) {
        this.Grade = n;
    }
    public int getGrade() {
        return this.Grade;
    }
}
